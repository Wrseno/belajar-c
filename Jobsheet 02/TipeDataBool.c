#include <stdio.h>
#include <stdbool.h>

int main() {
    bool semangat;

    semangat = true;
    printf("Semangat = %d\n", semangat);

    semangat = false;
    printf("Semangat = %d\n", semangat);

    semangat = 1;
    printf("Semangat = %d\n", semangat);

    semangat = 0;
    printf("Semangat = %d\n", semangat);
}